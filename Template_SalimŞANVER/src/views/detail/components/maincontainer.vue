<script>
export default {
    name: 'maincontainerComp',
    created() {
        function countdown() {
            const now = new Date();
            const eventDate = new Date("2023-06-01");

            const currentTimestamp = now.getTime();
            const eventTimestamp = eventDate.getTime();

            const diff = eventTimestamp - currentTimestamp;
            const days = Math.floor(diff / (1000 * 60 * 60 * 24));
            const hours = Math.floor((diff / (1000 * 60 * 60)) % 24);
            const minutes = Math.floor((diff / 1000 / 60) % 60);
            const seconds = Math.floor((diff / 1000) % 60);

            document.getElementById("days").textContent = days;
            document.getElementById("hours").textContent = hours;
            document.getElementById("minutes").textContent = minutes;
            document.getElementById("seconds").textContent = seconds;
        }

        setInterval(countdown, 1000);



        window.addEventListener("scroll", function () {
            var navbar = document.querySelector("nav");
            if (window.scrollY > 40) { // sayfa 200px kaydırıldığında navbar sabitlenir
                navbar.classList.add("fixed", "top-0", "z-50");
            } else {
                navbar.classList.remove("fixed", "top-0", "z-50");
            }
        })
    },






}

